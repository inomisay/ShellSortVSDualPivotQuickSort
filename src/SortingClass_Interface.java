
public interface SortingClass_Interface {

	/**
     * Sorts the given array using the ShellSort algorithm.
     * 
     * @param arrayToSort the array to be sorted
     */
    void shellSort(int[] arrayToSort);
    
    /**
     * Sorts the given array using the Dual Pivot QuickSort algorithm.
     * 
     * @param arrayToSort the array to be sorted
     */
    void dualPivotQuickSort(int[] arrayToSort);
	
}
